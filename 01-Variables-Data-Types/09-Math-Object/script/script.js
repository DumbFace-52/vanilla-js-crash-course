// Working with the Math Object

// Example 1
let num1;

// Square-Root
num1 = Math.sqrt(16);
// Absolute Value
num1 = Math.abs(-10);
num1 = Math.abs(10);
// Round
num1 = Math.round(1.6);
num1 = Math.round(1.4);
// Floor/ Round-Down
num1 = Math.floor(1.6);
num1 = Math.floor(1.4);
// Ceil/ Round-Up
num1 = Math.ceil(1.6);
num1 = Math.ceil(1.4);
// Power
num1 = Math.pow(2, 3);
// Max
num1 = Math.max(10, 5, 20, 30);
// Min
num1 = Math.min(10, 5, 20, 30);
// Random
num1 = Math.random();
num1 = Math.random() * 100 + 1;
num1 = Math.floor(Math.random() * 100 + 1);

console.log(num1);